
def main():
    """หาจำนวนกระต่ายและนก"""
    a = int(input())  # จำนวนสัตว์ทั้งหมด
    b = int(input())  # จำนวนขาทั้งหมด

    # สมการ: 4r + 2b = จำนวนขาทั้งหมด และ r + b = จำนวนสัตว์ทั้งหมด
    # คำนวณว่ามีกระต่าย (r) และนก (b) กี่ตัว
    for r in range(a + 1):  # r = จำนวนกระต่าย
        birds = a - r  # จำนวนสัตว์ - กระต่าย = นก
        if 4 * r + 2 * birds == b:
            print(r, birds)
            return

    # ถ้าหาคำตอบไม่ได้แสดงว่าเป็นไปไม่ได้
    print("Impossible")

main()
