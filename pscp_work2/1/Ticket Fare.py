def calculate_fare():
    # รับค่าจาก input
    N = int(input())  # จำนวนสถานีทั้งหมด
    a = int(input())  # จำนวนสถานีแรกที่คิดค่าโดยสารคงที่
    b = int(input())  # ค่าโดยสารคงที่สำหรับ a สถานีแรก
    c = int(input())  # จำนวนสถานีระดับที่ 2
    d = int(input())  # ค่าโดยสารต่อสถานีในระดับที่ 2
    e = int(input())  # ค่าโดยสารต่อสถานีในระดับที่ 3
    f = int(input())  # สถานีต้นทาง
    g = int(input())  # สถานีปลายทาง

    # ตรวจสอบว่าค่า f และ g อยู่ในขอบเขตหรือไม่
    if f < 1 or g < 1 or f > N or g > N:
        print("Error")
        return

    # คำนวณจำนวนสถานีที่เดินทาง
    distance = abs(g - f)
    
    # ถ้าเดินทาง 0 สถานี (ลงสถานีเดิม) ค่าโดยสารคือ b บาทตามระดับที่ 1
    if distance == 0:
        print(b)
        return

    # คำนวณค่าโดยสารตามระยะทาง
    fare = 0

    # ถ้าเดินทางใน a สถานีแรก
    if distance <= a:
        fare = b
    else:
        # เดินทางเกิน a สถานี
        fare = b  # ค่าโดยสารใน a สถานีแรก
        distance -= a  # หักสถานีที่เดินทางในระดับที่ 1

        # เดินทางในระดับที่ 2 (ไม่เกิน c สถานี)
        if distance <= c:
            fare += distance * d
        else:
            # เดินทางเกิน c สถานี ต้องคำนวณระดับที่ 3
            fare += c * d  # ค่าโดยสารในระดับที่ 2
            distance -= c  # หักสถานีในระดับที่ 2

            # ค่าโดยสารในระดับที่ 3
            fare += distance * e

    # แสดงผลค่าโดยสารที่คำนวณได้
    print(fare)

calculate_fare()
